def test_analyst_data_conversion():
    """测试分析师数据转换是否导致数值变化"""
    print("开始测试分析师数据转换...")
    
    # 获取原始数据
    analyst_id = "11000276336"  # 使用示例中看到的有效ID
    print(f"获取分析师 {analyst_id} 的数据...")
    
    try:
        original_data = ak.stock_analyst_detail_em(analyst_id=analyst_id, indicator="最新跟踪成分股")
        # 检查数据是否为空
        if original_data is None or original_data.empty:
            print("警告: 获取到的数据为空或None")
            return 
               
        print("原始数据:", original_data)

        print("原始数据类型:", type(original_data))
        print("原始数据形状:", original_data.shape if hasattr(original_data, 'shape') else 'N/A')
        

        
        
        # 转换为字典列表格式
        original_records = original_data.to_dict('records')
        print(f"原始记录数: {len(original_records)}")
        
        if original_records:
            print("原始第一条记录:")
            for key, value in original_records[0].items():
                print(f"  {key}: {value} (类型: {type(value)})")
        
        # 应用 _convert_dates_to_strings 转换
        print("\n应用 _convert_dates_to_strings 转换...")
        converted_records = _convert_dates_to_strings(original_records)
        
        if converted_records:
            print("转换后第一条记录:")
            for key, value in converted_records[0].items():
                print(f"  {key}: {value} (类型: {type(value)})")
        
        # 比较原始数据和转换后的数据
        print("\n比较原始数据和转换后的数据...")
        if len(original_records) > 0 and len(converted_records) > 0:
            original_first = original_records[0]
            converted_first = converted_records[0]
            
            print("字段对比:")
            for key in original_first.keys():
                orig_val = original_first[key]
                conv_val = converted_first.get(key, "MISSING")
                
                orig_type = type(orig_val)
                conv_type = type(conv_val)
                
                # 检查数值是否发生变化
                values_equal = orig_val == conv_val
                if not values_equal:
                    print(f"  {key}: 类型变化 {orig_type} -> {conv_type}, 值变化 {orig_val} -> {conv_val}")
                else:
                    print(f"  {key}: OK ({orig_val})")
        
        # 测试缓存功能
        print("\n测试缓存功能...")
        cache_key = f"test_analyst_{analyst_id}"
        
        # 设置缓存
        set_analyst_cache_data(cache_key, original_records)
        print("数据已存入缓存")
        
        # 从缓存读取
        cached_data = get_analyst_cached_data(cache_key)
        print(f"从缓存读取的数据记录数: {len(cached_data) if cached_data else 0}")
        
        if cached_data and len(cached_data) > 0:
            print("缓存第一条记录:")
            for key, value in cached_data[0].items():
                print(f"  {key}: {value} (类型: {type(value)})")
        
        # 比较原始数据和缓存数据
        print("\n比较原始数据和缓存数据...")
        if len(original_records) > 0 and len(cached_data) > 0:
            original_first = original_records[0]
            cached_first = cached_data[0]
            
            print("字段对比 (原始 vs 缓存):")
            for key in original_first.keys():
                orig_val = original_first[key]
                cached_val = cached_first.get(key, "MISSING")
                
                orig_type = type(orig_val)
                cached_type = type(cached_val)
                
                # 检查数值是否发生变化
                values_equal = orig_val == cached_val
                if not values_equal:
                    print(f"  {key}: 类型变化 {orig_type} -> {cached_type}, 值变化 {orig_val} -> {cached_val}")
                else:
                    print(f"  {key}: OK ({orig_val})")
        
        print("\n测试完成")
        
    except Exception as e:
        print(f"测试过程中出现错误: {e}")
        print(f"错误类型: {type(e).__name__}")
        import traceback
        traceback.print_exc()
        return  # 如果API调用失败，直接返回，避免后续处理




